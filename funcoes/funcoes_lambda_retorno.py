def soma(a,b): # aqui temos valores de retorno usando return como palavra-chave
    return a * b
resultado = soma(5,5)
print(resultado)

quadrado = lambda x:x ** 2 # função anônima(lambda) são funcoes sem nome denifinidas em uma úica linha. comumente usado para funcões simples e consisas.
print(quadrado(5))

